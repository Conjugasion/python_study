def findIncreaseStr(str):
    dp = [0] * len(str)
    dp[0] = 1
    for i in range(1, len(str)):
        if str[i] >= str[i - 1]:
            dp[i] = dp[i - 1] + 1
        else:
            dp[i] = 1
    result = list()
    for i in range(1, len(dp)):
        if dp[i] <= dp[i - 1] & dp[i - 1] >= 5:
            result.append(str[i - dp[i - 1]:i])
        # 坑 要加括号！
        if (i == len(dp) - 1) & dp[i] >= 5:
            result.append(str[i + 1 - dp[i]:i + 1])
    for r in result:
        print(r)


